          turns although since 2017, Dalian University of Technology has
          replaced UESTC of China. Undergraduate and graduate students are
          encouraged to participate in this academic meeting as well as the
          faculty members. It will be a great opportunity for sharing their
          university lives and recent research results in the area of
          electronics and communications.
